#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = resolve(__filename, "..");

const INPUT_PATH = resolve(
  __dirname,
  "../packages/editor-core/src/model/builtin-behaviors.yaml",
);
const OUTPUT_PATH = resolve(
  __dirname,
  "../packages/editor-core/src/model/generatedBuiltinBehaviorCatalog.ts",
);

const VALID_TYPES = new Set(["action", "invoke", "condition", "observer"]);

const unquoteValue = (value) => {
  if (value.length >= 2) {
    if (value.startsWith('"') && value.endsWith('"')) {
      return value.slice(1, -1);
    }
    if (value.startsWith("'") && value.endsWith("'")) {
      return value.slice(1, -1);
    }
  }
  return value;
};

const parsePair = (pair, lineNumber) => {
  const separatorIndex = pair.indexOf(":");
  if (separatorIndex <= 0) {
    throw new Error(`Invalid key/value pair at line ${lineNumber}: '${pair}'`);
  }

  const key = pair.slice(0, separatorIndex).trim();
  const rawValue = pair.slice(separatorIndex + 1).trim();

  if (!key) {
    throw new Error(`Empty key at line ${lineNumber}`);
  }

  if (!rawValue) {
    throw new Error(`Empty value for key '${key}' at line ${lineNumber}`);
  }

  return {
    key,
    value: unquoteValue(rawValue),
  };
};

const parseBuiltinBehaviorYaml = (raw) => {
  const lines = raw.split(/\r?\n/);
  const behaviors = [];

  let inBehaviors = false;
  let current = null;

  lines.forEach((line, index) => {
    const lineNumber = index + 1;
    const trimmed = line.trim();

    if (!trimmed || trimmed.startsWith("#")) {
      return;
    }

    if (!inBehaviors) {
      if (trimmed === "behaviors:") {
        inBehaviors = true;
        return;
      }

      throw new Error(
        `Unexpected content before 'behaviors:' at line ${lineNumber}: '${trimmed}'`,
      );
    }

    if (trimmed.startsWith("- ")) {
      if (current) {
        behaviors.push(current);
      }
      current = {};
      const remainder = trimmed.slice(2).trim();
      if (remainder) {
        const pair = parsePair(remainder, lineNumber);
        current[pair.key] = pair.value;
      }
      return;
    }

    if (!current) {
      throw new Error(`Expected list item at line ${lineNumber}`);
    }

    const pair = parsePair(trimmed, lineNumber);
    current[pair.key] = pair.value;
  });

  if (current) {
    behaviors.push(current);
  }

  if (!inBehaviors) {
    throw new Error("Missing top-level 'behaviors:' section");
  }

  return behaviors;
};

const validateBehaviors = (behaviors) => {
  const seenSrc = new Set();

  behaviors.forEach((item, index) => {
    const context = `item #${index + 1}`;

    if (!item.src) {
      throw new Error(`Missing 'src' in ${context}`);
    }
    if (!item.type) {
      throw new Error(`Missing 'type' in ${context}`);
    }
    if (!item.descriptionKey) {
      throw new Error(`Missing 'descriptionKey' in ${context}`);
    }
    if (!item.descriptionFallback) {
      throw new Error(`Missing 'descriptionFallback' in ${context}`);
    }

    if (!VALID_TYPES.has(item.type)) {
      throw new Error(`Invalid type '${item.type}' in ${context}`);
    }

    if (seenSrc.has(item.src)) {
      throw new Error(`Duplicated src '${item.src}' in catalog`);
    }

    seenSrc.add(item.src);
  });
};

const generateTsContent = (behaviors) => {
  const serialized = JSON.stringify(behaviors, null, 2);

  return [
    "// AUTO-GENERATED FILE. DO NOT EDIT DIRECTLY.",
    "// Generated by studio/scripts/generate-builtin-behaviors.mjs",
    "",
    'import type { BehaviorType } from "../types";',
    "",
    "export interface BuiltinBehaviorCatalogItem {",
    "  src: string;",
    "  type: BehaviorType;",
    "  descriptionKey: string;",
    "  descriptionFallback: string;",
    "}",
    "",
    "export const BUILTIN_BEHAVIOR_CATALOG =",
    `${serialized} as const satisfies ReadonlyArray<BuiltinBehaviorCatalogItem>;`,
    "",
  ].join("\n");
};

const main = () => {
  const raw = readFileSync(INPUT_PATH, "utf8");
  const behaviors = parseBuiltinBehaviorYaml(raw);
  validateBehaviors(behaviors);

  const output = generateTsContent(behaviors);
  writeFileSync(OUTPUT_PATH, output, "utf8");

  console.log(`Generated ${OUTPUT_PATH}`);
};

main();
